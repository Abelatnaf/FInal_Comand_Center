"use client";

import { useEffect, useState } from "react";

type Theme = "light" | "dark" | "system";

const OPTIONS: { value: Theme; label: string; Icon: (p: { size?: number }) => React.ReactElement }[] = [
  { value: "light", label: "Light", Icon: SunIcon },
  { value: "dark", label: "Dark", Icon: MoonIcon },
  { value: "system", label: "Auto", Icon: GearIcon },
];

function apply(theme: Theme) {
  const root = document.documentElement;
  if (theme === "system") {
    delete root.dataset.theme;
  } else {
    root.dataset.theme = theme;
  }
}

export function ThemeToggle() {
  // Start from whatever the no-flash init script already resolved, so the
  // control renders in the correct position without a flicker.
  const [theme, setTheme] = useState<Theme>("system");

  // Syncing from localStorage (an external system) on mount is exactly what
  // effects are for; the initial render intentionally shows the default until
  // the persisted choice is read.
  useEffect(() => {
    const stored = (localStorage.getItem("theme") as Theme | null) ?? "system";
    // eslint-disable-next-line react-hooks/set-state-in-effect
    setTheme(stored);
  }, []);

  function choose(next: Theme) {
    setTheme(next);
    if (next === "system") localStorage.removeItem("theme");
    else localStorage.setItem("theme", next);
    apply(next);
  }

  return (
    <div className="segmented w-full max-w-[320px]">
      {OPTIONS.map(({ value, label, Icon }) => (
        <button
          key={value}
          type="button"
          data-active={theme === value}
          onClick={() => choose(value)}
          className="flex items-center justify-center gap-1.5"
        >
          <Icon size={15} />
          {label}
        </button>
      ))}
    </div>
  );
}

